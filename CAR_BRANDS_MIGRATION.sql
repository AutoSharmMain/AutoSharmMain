-- Create car_brands table for OLX-style brand management
CREATE TABLE car_brands (
  id BIGSERIAL PRIMARY KEY,
  name VARCHAR(100) UNIQUE NOT NULL,
  logo_url VARCHAR(500),
  description TEXT,
  country VARCHAR(50),
  founded_year INT,
  is_active BOOLEAN DEFAULT TRUE,
  display_order INT DEFAULT 0,
  created_at TIMESTAMP DEFAULT NOW(),
  updated_at TIMESTAMP DEFAULT NOW()
);

-- Create index for faster brand lookups
CREATE INDEX idx_car_brands_name ON car_brands(name);
CREATE INDEX idx_car_brands_active ON car_brands(is_active);
CREATE INDEX idx_car_brands_display_order ON car_brands(display_order);

